# Python Operators Challenge

# Given Data:
x = 7
y = 4

# 1. Add 'x' and 'y' and store the result in a variable 'sum_result'.
sum_result = x+y
#print(sum_result)
# 2. Multiply 'x' by 3 and store the result in a variable 'multiply_result'.
multiply_result = x*3
#print(multiply_result)
# 3. Apply unary minus to 'y' and store the result in a variable 'negate_y'.
negate_y = -y
#print(negate_y)
# 4. Check if 'x' is greater than or equal to 'y' and store the result in a variable 'is_greater_or_equal'.
is_greater_or_equal = x >= y
#print(is_greater_or_equal)
# 5. Perform a logical AND between 'x' not equal to 0 and 'y' less than 10 and store the result in 'logical_result'.
logical_result = x != 0 and y < 10
#print(logical_result)
# Displaying the results
final = sum_result, multiply_result, negate_y, is_greater_or_equal, logical_result
